//WAP to which do bitwise OR operation of 5 and 7 and prints the values of the operation
public class BitwiseOperators {
    public static void main(String[] args) {
        // Perform bitwise OR operation between 5 and 7
        int result1 = 5 | 7;

        // Print the result
        System.out.println("Bitwise OR of 5 and 7: " + result1);

        // Perform bitwise AND operation between 5 and 7
        int result2 = 5 & 7;

        // Print the result
        System.out.println("Bitwise AND of 5 and 7: " + result2);

        // Perform bitwise XOR operation between 5 and 7
        int result3 = 5 ^ 7;

        // Print the result
        System.out.println("Bitwise XOR of 5 and 7: " + result3);
    }
}
